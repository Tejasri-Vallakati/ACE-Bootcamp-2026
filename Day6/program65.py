import numpy as np
arr=np.array([2,4,5,8,9])
print("Multiplication :",arr*2)#multiplication
print("Addition: ",arr+5)#addition
print("Power: ",arr**2)#power
print("Subtraction: ",arr-1)#subtraction
print("Division: ",arr/2)#division
print("Modulus: ",arr%3)#modulus
print(np.add(2,arr))
print(np.subtract(1,arr))
print(np.multiply(2,arr))
print(np.divide(2,arr))